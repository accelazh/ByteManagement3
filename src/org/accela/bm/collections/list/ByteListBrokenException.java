package org.accela.bm.collections.list;

import java.io.IOException;

public class ByteListBrokenException extends IOException
{
	private static final long serialVersionUID = 1L;

	public ByteListBrokenException()
	{
		super();
	}

	public ByteListBrokenException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public ByteListBrokenException(String message)
	{
		super(message);
	}

	public ByteListBrokenException(Throwable cause)
	{
		super(cause);
	}

}
